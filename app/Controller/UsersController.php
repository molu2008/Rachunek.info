<?php
App::uses('AppController', 'Controller');
/**
 * Users Controller
 *
 * @property User $User
 * @property PaginatorComponent $Paginator
 */
class UsersController extends AppController {

/**
 * Components
 *
 * @var array
 */


	public $components = array('Paginator');

/**
 * index method
 *
 * @return void
 */
	public function index() {
		$this->User->recursive = 0;
		$this->set('users', $this->Paginator->paginate());
	}


    public function beforeFilter(){
        parent::beforeFilter();
        $this->Auth->allow("add");
    }

    public function isAuthorized($user){
        if(in_array($this->action,array('edit' ,'delete'))){  // if we perform one of those actions
             if($user['id'] !=$this->request->params['pass'][0]){ // if user id is not = to user id that we like to perform on operations
                   return false;
             }
        }
        return true;
    }

    public function login(){

        if($this->request->is('post')){
           if( $this->Auth->login()){ // successfull login
             //  $this->Session->setFlash('wszytsko O.K.');
                $this->redirect($this->Auth->redirect()) ;  // miejsce przekierowanai zdefiniowane w appcontroller
           }else{
               $this->Session->setFlash('twoj login lub hasÅ‚o sÄ… niepoprawne ');
               // miejsce przekierowanai zdefiniowane w appcontroller
           }
        }
    }

    public function logout(){
        $this->redirect($this->Auth->logout());
    }



/**
 * view method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function view($id = null) {
		if (!$this->User->exists($id)) {
			throw new NotFoundException(__('Invalid user'));
		}
		$options = array('conditions' => array('User.' . $this->User->primaryKey => $id));
		$this->set('user', $this->User->find('first', $options));
	}

/**
 * add method
 *
 * @return void
 */
	public function add() {
		if ($this->request->is('post')) {
			$this->User->create();
			if ($this->User->save($this->request->data)) {
				$this->Session->setFlash(__('The user has been saved.'));
				return $this->redirect(array('action' => 'index'));
			} else {
				$this->Session->setFlash(__('The user could not be saved. Please, try again.'));
			}
		}
		$personalDatas = $this->User->PersonalData->find('list');
		$this->set(compact('personalDatas'));
	}

/**
 * edit method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function edit($id = null) {
		if (!$this->User->exists($id)) {
			throw new NotFoundException(__('Invalid user'));
		}
		if ($this->request->is(array('post', 'put'))) {
			if ($this->User->save($this->request->data)) {
				$this->Session->setFlash(__('The user has been saved.'));
				return $this->redirect(array('action' => 'index'));
			} else {
				$this->Session->setFlash(__('The user could not be saved. Please, try again.'));
			}
		} else {
			$options = array('conditions' => array('User.' . $this->User->primaryKey => $id));
			$this->request->data = $this->User->find('first', $options);
		}
		$personalDatas = $this->User->PersonalData->find('list');
		$this->set(compact('personalDatas'));
	}

/**
 * delete method
 *
 * @throws NotFoundException
 * @param string $id
 * @return void
 */
	public function delete($id = null) {
		$this->User->id = $id;
		if (!$this->User->exists()) {
			throw new NotFoundException(__('Invalid user'));
		}
		$this->request->onlyAllow('post', 'delete');
		if ($this->User->delete()) {
			$this->Session->setFlash(__('The user has been deleted.'));
		} else {
			$this->Session->setFlash(__('The user could not be deleted. Please, try again.'));
		}
		return $this->redirect(array('action' => 'index'));
	}}
